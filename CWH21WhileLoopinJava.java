package com.dhavalkurkutiya;

public class CWH21WhileLoopinJava {
  public static void main(String[] args) {
    int i = 100;
    while (i <= 200) {
      System.out.print(i + "\n");
      i++;
    }
    System.out.println("Finish While Loop");
    while (true) {
      System.out.println("I am Infinet");
      break;
    }
    
  }
}
